        <ul>
          <li>
            人脸识别是近年来模式识别、图像处理、机器视觉、神经网络以及认知科学等领域研究的热点课题之一，被广泛应用于公共安全（罪犯识别等）、安全验证系统、信用卡验证、医学、档案管理、视频会议、人机交互系统等各个方面。
          </li>
          <li>
            而本系统利用人脸识别技术，简化了线上考试身份认证的流程，通过人工智能技术同时节约了教师用户和学生用户的认证时间，同时通过不定期抓拍检测节约了教师用户的监考成本。同时该技术的可靠性也能够保证防作弊效果的有效实施，基于已经经过开源市场检验的成熟人脸识别技术，能够有效的防止线上考试的作弊行为。
          </li>
