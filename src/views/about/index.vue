        <h3>2、模块设计</h3>
        <ul>
          <li>
            基于教考分离的考试系统是基于人工智能技术、大数据分析、云计算服务等新媒体技术开发的新一代在线考试系统。该系统基于在线教学平台构建的考试功能体系涵盖了目前各类院校的常用考试流程和功能使用需求，针对教考分离模式进行深化设计，各功能模块统筹规划且支持独立运行，并提供完备的数据安全和运维保障服务。
          </li>
          <li>
            随着线上教学系统的普遍使用，对于基于教考分离的考试系统的需求的复杂度和难度也越来越高。本系统对需要包含完整的考试流程管理功能进行相应的模块设计：建立题库、组卷、考试、监考、批阅和考试结果统计等。
          </li>
          <li>
            系统管理模块包括功能模块管理、组织机构管理、角色权限管理和操作日志管理。功能模块管理需要对考试系统功能进行多样化管理，本系统通过分别建立学生端和教师端双子系统，给教师和学生分别提供以差异化功能组件整合形成的完全体系统；组织机构管理则需要对高校、专业、班级三级单位进行梯度化管理，在每一个梯度设置相应的组织管理机制，分别在不同层级对普通用户进行高效化、有序化管理；角色权限管理则是系统中重要的安全和性能保障需求，对于不同角色的权限进行相应程度上的限制，在安全层面上能够做到有效防止越权行为的产生，并且对不同级别和身份的用户进行相应权限的赋予，能够有效优化用户操作的效率和系统信息安全的层级化防护；操作日志管理是提供给系统管理员用户所设置的日志管理方式，能够有效从后台查询相应的普通用户的操作日志并有利于管理员用户对普通用户进行系统化管理，从而达到高效管理多角色用户的优秀效果。
          </li>
          <li>
            用户管理模块包括管理员用户管理、教师用户、学生用户系统管理，管理员用户人工对相应的教师用户和学生用户进行管理，同时可以支持管理员用户模拟教师、学生角色登录已经进行一些相应功能的操作，并且在提供给管理员用户以高权限以完成一些特殊事件的处理。平台系统本身能够对教师用户和学生用户进行智能化、系统化管理，系统能够通过不同用户的特定标签来区分不同身份的用户并对相应用户群进行自动化管理，此外不同用户还有其相应的身份认证系统：人脸识别认证管理功能，支持人脸采集和照片采集的认证方式，通过利用人脸识别的高效性和准确性，对不同用户进行快速、精准的身份鉴定。
          </li>
          <li>
            题库模块实现试题分类管理、题库建设、题库个性化管理、题库安全管理和审批管理。题库需支持考试中绝大多数题型，题型包括但不限于：单选、多选、填空、判断、名词解析、论述、计算、分录、资料、连线、投票、排序、完形填空、阅读理解、综合、口语、听力、程序等多种题型，并对不同的题型施行分类管理；题库建设方式分为两种，第一种是以系统题库为主，系统根据教学大纲和课程体系智能化构建系统题库，学生用户和教师用户可以在系统题库的基础上进行更新和完善形成新的题库。同时，系统还提供给学生用户和教师用户个性化题库管理的方式。对于学生用户，系统提供错题库进行斩错训练，能够有效提高学生用户对考试错题的反思和理解，并帮助学生用户进行进一步温习和提高，对于教师用户，系统提供课程私有题库并给与教师用户管理题库的高自主权，教师用户可以对题库中试题数量、试题类型等题库数据进行个性化修改；在题库安全管理中，每个用户的题库均需要进行安全查验，有效防止个人用户题库的泄漏和丢失，系统会对题库中异常操作进行自动巡查，并按照相应预案对管理员用户和个人用户进行信息反馈；系统对个人题库更新和完善审批进行智能化管理，首先利用人工智能系统对更新和完善的题库进行智能纠错和违规内容报错，将纠错内容信息反馈给个人用户方便个人用户更高效完成更完善题库的组建，将违规内容同时反馈给系统管理员和个人用户，以便于系统管理员及时的人工审查和个人用户的自我整改。
          </li>
          <li>
            试卷管理模块提供手动组卷和智能组卷方式，手动组卷由教师用户从题库中手动选择题目，并对题目进行排序和赋予分值，最终系统根据教师用户提供的题目信息进行试卷的生成。而智能组卷则是只需要教师用户对试卷的参数进行配置，如提供相应题型比例、题目难度、题目分值等信息，系统即可自动进行组卷，同时教师用户可以选择差异化组卷，使不同学生用户接受到相同难度的试卷但是具体题目不同，从根源上防止了学生用户在线上考试中的作弊行为。同时教师用户可以对已创建的试卷进行封存、预览、编辑、复制、发布考试、删除、分配教师等管理操作，使得教师用户可以高效操作试卷，节约了教师用户对试卷操作的时间成本。
          </li>
          <li>
            考试发布管理模块教师用户可以对考试过程进行各项参数设置，包括考试发放对象用户、考试基本信息设置（考试时间、答题时长、限时进入、限时提交、考试批次、考试场次）、防作弊设置、高级设置和考试通知发放。
          </li>
          <li>
            考试管理模块可以查看未开始、进行中、已结束的所有考试，以列表形式呈现给用户。教师用户针对每次考试可以进行查看考试详情、管理考生、在线监考、管理监考老师、批阅试卷以及删除考试等操作。同时监考数据支持导出，可用于考试数据分析及存档。
          </li>
          <li>
            考务管理模块可以实现考务课程管理、考试类型管理、考试批次、排考数据管理以及未发布考试管理。管理员用户和权限较高的教师用户可以对考务课程进行管理，按照考务课程和考试类型进行分类管理，统筹学校、专业、班级三个层次的考试批次，根据系统推荐的智能化排序对排考数据进行统一化管理，并对未发布考试进行高精度算法排序，最终形成高效的考务管理过程。
          </li>
          <li>
            阅卷管理模块提供个性化阅卷设置，系统可以智能实现客观题自动批阅，主观题由阅卷老师批阅，同时支持生生互评等批阅方式，提高阅卷效率，有效减轻主观题教师用户的工作量，也满足不同形式的阅卷需求。阅卷后成绩自动记录，支持成绩导出、试卷导出、考试附件打包导出、数据存档等功能，方便考试数据分析以及试卷校对等功能。
          </li>
          <li>
            成绩管理模块支持对线上成绩和线下成绩的管理，教师端可以手动添加线下或平时成绩，而平台则对线上成绩自动进行存储，并将两部分成绩统一交付给教师用户进行管理，在减轻教师用户统计成绩的时间之时也避免了因为人工失误导致的一系列成绩管理问题。
          </li>
          <li>
            统计分析模块，对考试系统运行的整体情况进行综合分析，通过提供给老师班级整体数据、各题型得分情况、各考试范围内容得分情况等多维化数据，让教师用户能够更加清晰和直观的掌握考试相关数据，有利于教师用户根据考试结果对教学进行相应的改进和对考试内容以及难度的有机调整。同时系统还对学生用户的考试试卷进行分析，对比班级和自身试卷结果在题型、考试范围等多角度进行智能分析并提供个人化学习和调整建议。
          </li>
        </ul>
