          </li>
          <li>
            教考分离是教育学中术语，其目的是将教学和考试分开，在实际教学和考试中，任课老师不参与所任课程的考试的一系列流程。教考分离强调严格按教学大纲教学，根据培养目标、教学目的、教学大纲，制定考核大纲，建立一套包括试题库、自动命题、阅卷、评分、考试分析、成绩管理等各种规章制度完备的考核管理系统。相对于教考合一而言，教考分离有利于教学考核的公平、公正，有利于教学评价的科学、有效，有利于教风、学风的根本好转。对于学生而言，教考分离会帮助学生认识到，考核成绩取决于对所学课程掌握与理解水平的高低，能促使其树立端正的学习态度，努力学习并掌握课程的基本内容、重点内容，经受课程学习结束后的考试检验。而对于教师而言，实行教考分离，能够使教师自觉地按照课程教学大纲和基本要求组织教学，注重教学研究，改进教学方法，求得好的教学效果。
          </li>
          <li>
            而基于教考分离的考试系统，同时满足了规范教学工作和适应教学管理制度改革的需要，以教学大纲为依据进行教学和考核，统一命题、集体流水评卷的教考分离模式，有利于充分发挥考试在教学工作中的作用，使教学工作规范有序地进行，从而最终形成重教重学的良好氛围，不断提高学校的教学质量。
          </li>
          <li>
            同样的，近些年来习近平总书记始终坚持把教育放在社会主义现代化建设中的优先发展战略地位。而教育部颁发的《关于加快建设高水平本科教育全面提高人才培养能力的意见》（新时代高教四十条）中，对于考试体系指明方向“加强考试管理，严格过程考核……以考辅教、以考促学。”教育大数据在推送教育改革与发展方面的战略地位凸显，以教育大数据采集、评价分析和应用为代表的技术体系与平台已成为推送教育改革的重要载体和前沿阵地之一。
