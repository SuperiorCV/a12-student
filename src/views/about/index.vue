        <h2>（三）、未来展望</h2>
        <ul>
          <li>
            由于时间和成本上的限制，本平台对基于教考分离系统的功能完成有限，以下是本团队对项目进行未来的设计和展望。
          </li>
          <li>
            在下一代版本中的教考分离系统中，我们计划对系统功能进行进一步丰富。
            在题库管理系统中，下一代版本系统题库要支持无限层级创建题库分类，支持对试题进行批量修改、删除、导出等操作，支持按照题型、难易度、创建时间、关键词输入对题库中的试题进行筛选搜索；支持使用Word、Excel、TXT模板批量导入试题，支持可视化批量新增试题。
          </li>
          <li>
            在用户和考试管理系统中，下一代版本系统要支持自主添加考生信息或使用Excel模板批量导入考生信息，支持不限层级创建组织架构；支持多种安排考试的方式，有免登录、口令、免登录+口令、账号密码这四种考试授权方式；考生可以通过二维码、链接、以及微信小程序进入考生中心，查看自己被安排的考试，以及自己参加过的所有考试记录及成绩。
          </li>
          <li>
            在考试管理系统中，除支持创建正式的线上考试外，平台还具有模拟考试+练习考试+正式考试多种组合考试模式。满足各类型线上刷题需求。模拟考试还原正式考试场景，帮助考生锻炼自己的答题节奏，更好的掌握考场状态。练习模式具有顺序练习及随机练习两种模式，顺序练习将按照原出题顺序逐题练习，而随机练习模式将打破原有顺序灵活出题，帮助考生更加真实的检测知识点掌握情况。
          </li>
          <li>
            此外，下一代版本中考试系统支持多终端答题，可支持电脑PC端、手机移动端、微信公众号等平台答题，在教师用户发放试题后，系统自动生成链接和二维码，用户提供分享链接或二维码到微信、微信公众号、微博、QQ等，让考生可以更方便的扫码答题。
